/*
    Alkuehtoinen while-silmukka
    Esimerkissä tulostetaan luvut 0 - 4
*/

// Asetetaan laskurin alkuarvo
let i = 0;

// Silmukkaa suoritetaan kunnes i => 5
while( i < 5 ) {
    // Tulostetaan luku
    console.log(i);
    // Kasvatetaan laskurin arvoa yhdellä
    i++;
}

// Hypätään tänne kun ehto i < 5 ei ole totta